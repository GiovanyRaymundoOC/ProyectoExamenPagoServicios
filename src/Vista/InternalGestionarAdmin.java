    private void CargarAdmins() {
        Connection cn = Conexion.establecerConexion();
        DefaultTableModel model = new DefaultTableModel();

        String sql = "select id_administrador, nombre, apellido, email, telefono, fecha_registro from administradores";
        Statement st;

        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            InternalGestionarAdmin.tblAdmins = new JTable(model);
            InternalGestionarAdmin.jScrollPane1.setViewportView(InternalGestionarAdmin.tblAdmins);

            model.addColumn("id_Admin");
            model.addColumn("nombre");
            model.addColumn("apellido");
            model.addColumn("email");
            model.addColumn("telefono");
            model.addColumn("fecha_registro");

            while (rs.next()) {
                Object fila[] = new Object[6];
                for (int i = 0; i < 6; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            cn.close();

        } catch (SQLException e) {
            System.out.println("Error al cargar" + e);
        }

        tblAdmins.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int filaP = tblAdmins.rowAtPoint(e.getPoint());
                int columnaP = 0;

                if (filaP > -1) {
                    idAdmin = (int) model.getValueAt(filaP, columnaP);
                    EnviarDatosAdmins(idAdmin);
                }
            }
        });
    }

    private void EnviarDatosAdmins(int idAdmin) {
        try {
            Connection cn = Conexion.establecerConexion();
            PreparedStatement pst = cn.prepareStatement("select * from administradores where id_administrador = '" + idAdmin + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txtNombre.setText(rs.getString("nombre"));
                txtApellido.setText(rs.getString("apellido"));
                txtCorreo.setText(rs.getString("email"));
                txtTelefono.setText(rs.getString("telefono"));
            }
        } catch (SQLException e) {
            System.out.println("Error al seleccionar" + e);
        }
    }
