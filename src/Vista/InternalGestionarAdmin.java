    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(null, "¿Desea actualizar el Administrador?", "Confirmar Actualización", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            if (!v.validarCorreo(txtCorreo.getText())) {
                JOptionPane.showMessageDialog(null, "Correo invalido");
            } else {
                if (!txtNombre.getText().isEmpty() && !txtApellido.getText().isEmpty() && !txtCorreo.getText().isEmpty() && !txtTelefono.getText().isEmpty()) {
                    Administrador a = new Administrador();
                    ControlAdmin Ca = new ControlAdmin();
                    a.setNombre(txtNombre.getText().trim());
                    a.setApellido(txtApellido.getText().trim());
                    a.setEmail(txtCorreo.getText().trim());
                    a.setTelefono(txtTelefono.getText().trim());
                    if (Ca.Actualizar(a, idAdmin)) {
                        JOptionPane.showMessageDialog(null, "Admin actualizado");
                        txtNombre.setText("");
                        txtApellido.setText("");
                        txtCorreo.setText("");
                        txtTelefono.setText("");
                        this.CargarAdmins();
                    } else {
                        JOptionPane.showMessageDialog(null, "Error al actualizar");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Seleccione un Admin");
                }
            }
        }else {
            JOptionPane.showMessageDialog(null, "Actualización cancelada");

        }        
    }//GEN-LAST:event_btnActualizarMouseClicked

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(null, "¿Desea eliminar el Administrador?", "Confirmar Eliminación", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            if (!txtNombre.getText().isEmpty()) {
                Administrador a = new Administrador();
                ControlAdmin Ca = new ControlAdmin();
                a.setNombre(txtNombre.getText().trim());
                if (!Ca.Eliminar(idAdmin)) {
                    JOptionPane.showMessageDialog(null, "Admin eliminado");
                    txtNombre.setText("");
                    txtApellido.setText("");
                    txtCorreo.setText("");
                    txtTelefono.setText("");
                    this.CargarAdmins();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al eliminar");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione un Admin");
            }
        }else {
            JOptionPane.showMessageDialog(null, "Eliminación cancelada");

        }
    }//GEN-LAST:event_btnEliminarMouseClicked


